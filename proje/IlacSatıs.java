    private void ilacSatısTablosuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ilacSatısTablosuMouseClicked
        // Tabloya tıklanıldığında kişinin bilgileri yukarıdaki fieldlarda görünüyor.
        //Kaydet butonunu ve tc numarası değiştirilmez olması için Enable (False ) yapıldı.
        if (ilacSatısTablosu.getSelectedRow() < 0) {
            return;
        }

        String tcNo = (String) tableModel.getValueAt(ilacSatısTablosu.getSelectedRow(), 0);
        String url = "jdbc:mysql://" + this.host + ":" + this.port + "/" + this.dtb + "?useUnicode=true&characterEncoding=utf8";
        baglan();
        try {
            conn = DriverManager.getConnection(url, kullaniciAdi, parola);
            Statement stmt = conn.createStatement();
            String query = "select * from ilacsatisi WHERE tc=" + tcNo;
            ResultSet rs = stmt.executeQuery(query);
            while (rs.next()) {
                İlacGosterimi ig = new İlacGosterimi();
                ig.tc = rs.getString("tc");
                ig.ad = rs.getString("Ad_Soyad");
                ig.ilacAdi = rs.getString("ilacAdı");
                ig.doz = rs.getInt("ilacDozu");
                ig.fiyat = rs.getString("fiyat");

                tcNo_field.setText(ig.tc);
                adSoyad_field.setText(ig.ad);
                ilacLabel.setText(ig.ilacAdi);
                doz_spinner.setValue(ig.doz);
                ilacFiyati_field.setText(ig.fiyat);
                kaydet_button.setEnabled(false);
                tcNo_field.setEnabled(false);
            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_ilacSatısTablosuMouseClicked

