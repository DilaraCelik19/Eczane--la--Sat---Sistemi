    public void setRecordsToTable() {
     
       //Sayfa açılır açılmaz tablonun dolu olduğu gözükmesi için bu metodu constructora ekledim. 
        String url = "jdbc:mysql://" + this.host + ":" + this.port + "/" + this.dtb + "?useUnicode=true&characterEncoding=utf8";
        baglan();
        try {
            conn = DriverManager.getConnection(url, kullaniciAdi, parola);
            PreparedStatement pst = conn.prepareStatement("select * from ilacsatisi");
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                String tcNo = rs.getString("tc");
                String adSoyad = rs.getString("Ad_Soyad");
                String ilacAdi = rs.getString("ilacAdı");
                int doz = rs.getInt("ilacDozu");
                String fiyat = rs.getString("fiyat");
//Buradaki bilgileri alıp table 'ın satırlarına yazıyor.
                Object[] obj = {tcNo, adSoyad, ilacAdi, doz, fiyat};
                tableModel = (DefaultTableModel) ilacSatısTablosu.getModel();
                tableModel.addRow(obj);
            }
        } catch (Exception e) {
        }
    }

