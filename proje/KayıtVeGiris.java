    private void giris_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_giris_buttonActionPerformed
        // Recete no ya sadece sayı yazılmasını belirten bir regex bulunuyor.
        String receteNo = eReceteNo_field.getText();
        Pattern p = Pattern.compile("^\\d+");
        Matcher m = p.matcher(receteNo);
        if (!m.matches()) {
            JOptionPane.showMessageDialog(null, " Recete No boşluğuna harf yazılmaz");
            return;
        }
        //Ad soyad ,tc ,recete no nun alanı boş bırakılırsa uyarı verir.
     if(girisAdSoyad_field.getText().isEmpty() || girisTcNo_field.getText().isEmpty() || eReceteNo_field.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Boş kalan alanları doldurunuz!!", "UYARI", JOptionPane.INFORMATION_MESSAGE);
            return;
     }
     //ilaç satış frame açılır ve bilgiler constructora yazılanlardan ötürü direk o sayfaya basılır.
        IlacSatıs ilacSatıs = new IlacSatıs(girisAdSoyad_field.getText(), girisTcNo_field.getText());
        ilacSatıs.setVisible(true);

    }//GEN-LAST:event_giris_buttonActionPerformed

