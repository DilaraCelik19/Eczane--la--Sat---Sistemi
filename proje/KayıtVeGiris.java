    private void girisKaydet_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_girisKaydet_buttonActionPerformed
        // TODO add your handling code here:
        //tc boşluğunnun sayılardan ve 11 haneli olması gerektiğini gösteren bir regex.
        String tcNumara = girisAdSoyad_field.getText();

        Pattern p = Pattern.compile("\\d{10}[2468]");
        Matcher m2 = p.matcher(tcNumara);
        if (m2.matches()) {
            JOptionPane.showMessageDialog(null, "TC yazımında hata var");
            return;
        }

        String tc = girisTcNo_field.getText();
        if (baglanti.giristekiKisiAyniMi(tc)) {

            JOptionPane.showMessageDialog(null, "Kişi daha önce kayıt edilmis", "WARNİNG", JOptionPane.WARNING_MESSAGE);
        } else {
            String tcNo = girisTcNo_field.getText();
            String adSoyad = girisAdSoyad_field.getText();
            String eReceteNo = eReceteNo_field.getText();
            baglanti.girisKaydet(tcNo, adSoyad, eReceteNo);
        }

    }//GEN-LAST:event_girisKaydet_buttonActionPerformed

