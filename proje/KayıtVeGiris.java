    private void kaydolKaydet_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kaydolKaydet_buttonActionPerformed
        // TODO add your handling code here:
  //Ad ve soyad da sadece harflerin kullanılması gerektiğini gösteren bir regex.
        String adSoyad = kayitAdSoyad_field.getText();
        Matcher matcher = Pattern.compile(".*\\d+.*").matcher(adSoyad);
        if (matcher.matches()) {
            JOptionPane.showMessageDialog(null, "Ad Soyad boşluğuna rakam yazılmaz");
            return;
        }

        if (kayitTcNo_field.getText().isEmpty() || kayitAdSoyad_field.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Boş kalan alanları doldurunuz!!", "UYARI", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String tc2 = kayitTcNo_field.getText();
        //Aşağıdaki metodu kontrol ederek hastanın kayıtlı olup olmadığını belirtiyor.
        if (baglanti.buHastaVarMi(tc2)) {
            JOptionPane.showMessageDialog(null, "Böyle bir hasta var.Tekrar kaydedilmez", "WARNİNG", JOptionPane.WARNING_MESSAGE);
        } else {
            String tc = kayitTcNo_field.getText();
            String ad = kayitAdSoyad_field.getText();

            String cinsiyet = "";
            if (kadin_radioButton.isSelected()) {
                cinsiyet = "Kadın";
            } else {
                cinsiyet = "Erkek";
            }
            //SimpleDateFormat sınıfını kullanarak stringe çevirdim.
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String dogumTarihi = sdf.format(dateChooser.getDate());
            baglanti.kaydolKaydet(tc, ad, cinsiyet, dogumTarihi);

        }


    }//GEN-LAST:event_kaydolKaydet_buttonActionPerformed

